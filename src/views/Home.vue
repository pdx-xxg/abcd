import { ref, onMounted, onUnmounted } from 'vue'
import { useRouter } from 'vue-router'

defineOptions({ name: 'Home' })

const router = useRouter()

const logoImage = new URL('@/assets/logo.png', import.meta.url).href
const logoImage4 = new URL('@/assets/logo4-DX7cGJ5Y.png', import.meta.url).href

const isScrolled = ref(false)
const activeSection = ref('')
const activeDropdown = ref('')

const navItems = [
  { id: 'about', label: 'å…³äºæ©™æœ' },
  { id: 'departments', label: 'å·¥ä½œéƒ¨é—¨' },
  { id: 'groups', label: 'å­¦ä¹ å°ç»„' },
  { id: 'quick-access', label: 'å¿«é€Ÿå…¥å£' },
]

const toggle = (key: string) => {
  activeDropdown.value = activeDropdown.value === key ? '' : key
}

const jump = (path: string) => {
  router.replace(path)
}

const scrollToSection = (id: string) => {
  const element = document.getElementById(id)
  if (element) {
    element.scrollIntoView({ behavior: 'smooth' })
    activeSection.value = id
  }
}

const scrollToTop = () => {
  window.scrollTo({ top: 0, behavior: 'smooth' })
  activeSection.value = ''
}

const handleScroll = () => {
  isScrolled.value = window.scrollY > 100

  // æ£€æµ‹å½“å‰å¯è§çš„section
  const sections = navItems.map(item => ({
    id: item.id,
    element: document.getElementById(item.id),
  }))

  const scrollPosition = window.scrollY + 200

  for (let i = sections.length - 1; i >= 0; i--) {
    const section = sections[i]
    if (section && section.element) {
      const offsetTop = section.element.offsetTop
      if (scrollPosition >= offsetTop) {
        activeSection.value = section.id
        break
      }
    }
  }

  // å¦‚æœæ»šåŠ¨åˆ°é¡¶éƒ¨ï¼Œæ¸…é™¤active
  if (window.scrollY < 100) {
    activeSection.value = ''
  }
}

const handleClickOutside = (e: MouseEvent) => {
  const target = e.target as HTMLElement
  if (!target.closest('.home-nav')) {
    activeDropdown.value = ''
  }
}

onMounted(() => {
  window.addEventListener('scroll', handleScroll)
  document.addEventListener('click', handleClickOutside)
  handleScroll() // åˆå§‹æ£€æŸ¥
})

onUnmounted(() => {
  window.removeEventListener('scroll', handleScroll)
  document.removeEventListener('click', handleClickOutside)
})
